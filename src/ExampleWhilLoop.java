public class ExampleWhilLoop {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		int i;
		int m = 0, n = 0;

		for (i = 0; i>5; i++) {
		
			System.out.println("oh my god");
			
		}
		
		
		while(m>5){
			System.out.println("i am while");
			m++;
		}

		
		do{
			System.out.println("i am do");
			n++;
		}while(n>5);
		
	}

}
